package com.example.mylibrary;

public class Test {

    /**
     * 整数加法.
     * @param a1 number
     * @param a2 number
     * @return number
     */
    public static int plus(int a1,int a2){
        return a1 + a2;
    }

    /**
     * 整数减法.
     * @param a1 number
     * @param a2 number
     * @return number
     */
    public static int sub(int a1,int a2){
        return a1 - a2;
    }
}
